DROP TABLE RPT_MONITOR_GIS CASCADE CONSTRAINTS PURGE;
CREATE TABLE RPT_MONITOR_GIS 
(	BUSINESS_DATE			DATE,
	GIS_DATETIME			TIMESTAMP, 
	OPER_ONLINE_COUNT		NUMBER, 
	NON_OPER_ONLINE_COUNT	NUMBER, 
	ONLINE_COUNT			NUMBER, 
	AREA_CODE				VARCHAR2(60),
	ORG_GROUP 				VARCHAR2(100),
	ORG_GROUP_ID			VARCHAR2(60)
) 
TABLESPACE TBS_OSECDJW_RPT
NOLOGGING;

CREATE INDEX RPT_MONITOR_GIS_IDX1 ON RPT_MONITOR_GIS (GIS_DATETIME) TABLESPACE TBS_OSECDJW_RPT NOLOGGING;

 
COMMENT ON TABLE RPT_MONITOR_GIS  IS '车辆监控-GIS监控';

COMMENT ON COLUMN RPT_MONITOR_GIS.GIS_DATETIME			IS '日期时间';
COMMENT ON COLUMN RPT_MONITOR_GIS.OPER_ONLINE_COUNT		IS '运营车辆在线数';
COMMENT ON COLUMN RPT_MONITOR_GIS.NON_OPER_ONLINE_COUNT	IS '非运营车辆在线数';
COMMENT ON COLUMN RPT_MONITOR_GIS.ONLINE_COUNT		    IS '车辆在线总数';
COMMENT ON COLUMN RPT_MONITOR_GIS.AREA_CODE		    	IS '区域类型';
COMMENT ON COLUMN RPT_MONITOR_GIS.ORG_GROUP		    	IS '区域名称';
COMMENT ON COLUMN RPT_MONITOR_GIS.ORG_GROUP_ID		   	IS '区域ID';
