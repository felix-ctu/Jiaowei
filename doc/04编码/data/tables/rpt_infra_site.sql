DROP TABLE RPT_INFRA_SITE CASCADE CONSTRAINTS PURGE;
CREATE TABLE RPT_INFRA_SITE
(	YEARMONTH				VARCHAR2(10), 
	SITE_COUNT				NUMBER, 
	SITE_SQUARE				NUMBER,
	SITE_VOLUME				NUMBER,
	DEMAND_ABLITY			NUMBER,
	SITE_OWNED_COUNT		NUMBER,
	SITE_RENT_COUNT			NUMBER,
	SITE_BORROW_COUNT		NUMBER,
	AREA_CODE				VARCHAR2(60),
	ORG_GROUP 				VARCHAR2(100),
	ORG_GROUP_ID 			VARCHAR2(100)
) 
TABLESPACE TBS_OSECDJW_RPT
NOLOGGING;

CREATE INDEX RPT_INFRA_SITE_IDX1 ON RPT_INFRA_SITE (YEARMONTH) TABLESPACE TBS_OSECDJW_RPT NOLOGGING;

 
COMMENT ON TABLE RPT_INFRA_SITE  IS '基础设施-公交场站';

COMMENT ON COLUMN RPT_INFRA_SITE.YEARMONTH			IS '月份';
COMMENT ON COLUMN RPT_INFRA_SITE.SITE_COUNT		    IS '场站数量';
COMMENT ON COLUMN RPT_INFRA_SITE.SITE_SQUARE		IS '场站面积';
COMMENT ON COLUMN RPT_INFRA_SITE.SITE_VOLUME		IS '场站停车能力';
COMMENT ON COLUMN RPT_INFRA_SITE.DEMAND_ABLITY	    IS '场站需求能力';
COMMENT ON COLUMN RPT_INFRA_SITE.SITE_OWNED_COUNT	IS '场站自有数量';
COMMENT ON COLUMN RPT_INFRA_SITE.SITE_RENT_COUNT	IS '场站租用数量';
COMMENT ON COLUMN RPT_INFRA_SITE.SITE_BORROW_COUNT	IS '场站借用数量';
COMMENT ON COLUMN RPT_INFRA_SITE.AREA_CODE		    IS '区域类型';
COMMENT ON COLUMN RPT_INFRA_SITE.ORG_GROUP		    IS '区域名称';
COMMENT ON COLUMN RPT_INFRA_SITE.ORG_GROUP_ID		IS '区域ID';

