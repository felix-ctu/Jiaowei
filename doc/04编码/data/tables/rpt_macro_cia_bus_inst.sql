
 
DROP TABLE RPT_MACRO_CIA_BUS_INST CASCADE CONSTRAINTS PURGE;
CREATE TABLE RPT_MACRO_CIA_BUS_INST 
(	BUSINESS_DATE		DATE,
    CIA_DATE 			DATE, 
	ROUTE_COUNT 		NUMBER, 
	ROUTE_LENGTH 		NUMBER, 
	SUBROUTE_COUNT		NUMBER,
	SITE_COUNT 			NUMBER, 
	STATION_COUNT 		NUMBER, 
	EMP_COUNT 			NUMBER, 
	DRIVER_COUNT 		NUMBER,
	AREA_CODE 			VARCHAR2(30),
	ORG_GROUP 			VARCHAR2(100),
	ORG_GROUP_ID 		VARCHAR2(60)
) 
TABLESPACE TBS_OSECDJW_RPT
NOLOGGING ;


CREATE INDEX RPT_MACRO_CIA_BUS_INST_IDX1 ON RPT_MACRO_CIA_BUS_INST (CIA_DATE) TABLESPACE TBS_OSECDJW_RPT NOLOGGING;


COMMENT ON TABLE RPT_MACRO_CIA_BUS_INST  IS '综合指标分析之公交设施';
COMMENT ON COLUMN RPT_MACRO_CIA_BUS_INST.CIA_DATE 			IS '指标日期';
COMMENT ON COLUMN RPT_MACRO_CIA_BUS_INST.ROUTE_COUNT 		IS '线路条数';
COMMENT ON COLUMN RPT_MACRO_CIA_BUS_INST.ROUTE_LENGTH 		IS '线路长度';
COMMENT ON COLUMN RPT_MACRO_CIA_BUS_INST.SUBROUTE_COUNT 	IS '子线路数量';
COMMENT ON COLUMN RPT_MACRO_CIA_BUS_INST.SITE_COUNT 		IS '站场数量';
COMMENT ON COLUMN RPT_MACRO_CIA_BUS_INST.STATION_COUNT	 	IS '站点数量';
COMMENT ON COLUMN RPT_MACRO_CIA_BUS_INST.EMP_COUNT 			IS '从业人员数量';
COMMENT ON COLUMN RPT_MACRO_CIA_BUS_INST.DRIVER_COUNT 		IS '司机数量';
COMMENT ON COLUMN RPT_MACRO_CIA_BUS_INST.AREA_CODE 			IS '区域类型';
COMMENT ON COLUMN RPT_MACRO_CIA_BUS_INST.ORG_GROUP 			IS '区域名称';
COMMENT ON COLUMN RPT_MACRO_CIA_BUS_INST.ORG_GROUP_ID 		IS '区域ID';

