COMMENT ON COLUMN RPT_MACRO_CIA_CAP_INVEST.OP_QTY IS '运营车辆数';
COMMENT ON COLUMN RPT_MACRO_CIA_CAP_INVEST.AREA_CODE IS '区域类型';
