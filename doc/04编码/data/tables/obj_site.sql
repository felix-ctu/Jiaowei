COMMENT ON COLUMN OBJ_SITE.LATITUDE		    	IS '纬度';
COMMENT ON COLUMN OBJ_SITE.CCTV			    	IS '视频监控';
COMMENT ON COLUMN OBJ_SITE.SITE_FROM		    IS '场所来源';
COMMENT ON COLUMN OBJ_SITE.SITE_USED_FOR	    IS '主要用途';
COMMENT ON COLUMN OBJ_SITE.ROUTE_NAME		    IS '途径线路';
COMMENT ON COLUMN OBJ_SITE.AREA_CODE		    IS '所属区域';
COMMENT ON COLUMN OBJ_SITE.REGION_AREA	    	IS '';
