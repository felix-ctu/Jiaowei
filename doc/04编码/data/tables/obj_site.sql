(	SITE_ID					VARCHAR2(60), 
	ORG_ID					VARCHAR2(60), 
	ORG_NAME				VARCHAR2(200),
	SITE_NAME				VARCHAR2(200), 
	SITE_TYPE				VARCHAR2(60), 
	LONGITUDE				VARCHAR2(60), 
	LATITUDE				VARCHAR2(60), 
	CCTV					VARCHAR2(10), 
	SITE_FROM				VARCHAR2(60), 
	SITE_USED_FOR			VARCHAR2(60), 
	ROUTE_NAME				VARCHAR2(2000), 
	AREA_CODE				VARCHAR2(60), 
	REGION_AREA				VARCHAR2(60),
	SITE_VOLUME				NUMBER,
	SITE_SQUARE				NUMBER,
	ORG_GROUP 				VARCHAR2(100),
	ORG_GROUP_ID 			VARCHAR2(100)
	
) 
TABLESPACE TBS_OSECDJW_RPT
NOLOGGING;

