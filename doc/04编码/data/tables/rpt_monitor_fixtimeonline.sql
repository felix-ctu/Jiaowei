DROP TABLE RPT_MONITOR_FIXTIMEONLINE CASCADE CONSTRAINTS PURGE;
CREATE TABLE RPT_MONITOR_FIXTIMEONLINE 
(	BUSINESS_DATE			DATE,
	OPER_DATE				DATE, 
	FIXED_TIME_SECTION		VARCHAR2(30),
	WEEKDAY					VARCHAR2(60), 
	GPS_ONLINE_COUNT		NUMBER, 
	OPER_ONLINE_COUNT		NUMBER, 
	NON_OPER_ONLINE_COUNT	NUMBER,  
	AREA_CODE				VARCHAR2(60),
	ORG_GROUP 				VARCHAR2(100),
	ORG_GROUP_ID 			VARCHAR2(60)
) 
TABLESPACE TBS_OSECDJW_RPT
NOLOGGING;

CREATE INDEX RPT_MONITOR_FIXTIMEONLINE_IDX1 ON RPT_MONITOR_FIXTIMEONLINE (OPER_DATE) TABLESPACE TBS_OSECDJW_RPT NOLOGGING;

 
COMMENT ON TABLE RPT_MONITOR_FIXTIMEONLINE  IS '车辆监控-固定时段车辆在线数量';

COMMENT ON COLUMN RPT_MONITOR_FIXTIMEONLINE.OPER_DATE					IS '运营日期';
COMMENT ON COLUMN RPT_MONITOR_FIXTIMEONLINE.FIXED_TIME_SECTION			IS '运营固定时间段';
COMMENT ON COLUMN RPT_MONITOR_FIXTIMEONLINE.WEEKDAY					    IS '周几';
COMMENT ON COLUMN RPT_MONITOR_FIXTIMEONLINE.GPS_ONLINE_COUNT		    IS '车载在线数量';
COMMENT ON COLUMN RPT_MONITOR_FIXTIMEONLINE.OPER_ONLINE_COUNT		    IS '运营在线数量';
COMMENT ON COLUMN RPT_MONITOR_FIXTIMEONLINE.NON_OPER_ONLINE_COUNT	    IS '非运营在线数量';
COMMENT ON COLUMN RPT_MONITOR_FIXTIMEONLINE.AREA_CODE				    IS '区域类型';
COMMENT ON COLUMN RPT_MONITOR_FIXTIMEONLINE.ORG_GROUP				    IS '区域名称';
COMMENT ON COLUMN RPT_MONITOR_FIXTIMEONLINE.ORG_GROUP_ID				IS '区域ID';

