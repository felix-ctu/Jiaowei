COMMENT ON COLUMN OBJ_EMPLOYEE.POSITION_TYPE 		IS '岗位类型';
COMMENT ON COLUMN OBJ_EMPLOYEE.SEX 					IS '性别';
COMMENT ON COLUMN OBJ_EMPLOYEE.START_DATE 			IS '加入时间';
COMMENT ON COLUMN OBJ_EMPLOYEE.LISENCE_TYPE 		IS '驾驶证类型';
COMMENT ON COLUMN OBJ_EMPLOYEE.ORG_NAME 			IS '所属公司ID';
COMMENT ON COLUMN OBJ_EMPLOYEE.ROUTE_ID 			IS '所属线路';
COMMENT ON COLUMN OBJ_EMPLOYEE.EMP_ID 				IS '员工编号';
COMMENT ON COLUMN OBJ_EMPLOYEE.DESIGNATION 			IS '职称';
COMMENT ON COLUMN OBJ_EMPLOYEE.IC_CARD 				IS 'IC卡号';
COMMENT ON COLUMN OBJ_EMPLOYEE.DRIVE_EXPERIENCE 	IS '驾龄';
COMMENT ON COLUMN OBJ_EMPLOYEE.CERTIFICATE_NUMBER	IS '营运服务证号';
COMMENT ON COLUMN OBJ_EMPLOYEE.DIPLOMA				IS '文化程度';
COMMENT ON COLUMN OBJ_EMPLOYEE.BIRTHDATE			IS '出生日期';
COMMENT ON COLUMN OBJ_EMPLOYEE.SALARY				IS '工资';
COMMENT ON COLUMN OBJ_EMPLOYEE.ORG_GROUP 			IS '区域名称';
