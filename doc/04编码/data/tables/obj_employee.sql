DROP TABLE OBJ_EMPLOYEE CASCADE CONSTRAINTS PURGE;
CREATE TABLE OBJ_EMPLOYEE 
(	ID						VARCHAR2(50), 
    EMP_NAME				VARCHAR2(50), 
    POSITION_TYPE			VARCHAR2(40), 
    SEX						VARCHAR2(4), 
    START_DATE				DATE,
    LISENCE_TYPE			VARCHAR2(50), 
    ORG_NAME				VARCHAR2(50), 
    ROUTE_ID				VARCHAR2(300), 
    EMP_ID					VARCHAR2(50), 
    DESIGNATION				VARCHAR2(50), 
    IC_CARD					VARCHAR2(50), 
    DRIVE_EXPERIENCE		NUMBER,
    CERTIFICATE_NUMBER		VARCHAR2(50), 
	DIPLOMA					VARCHAR2(10),
	BIRTHDATE				DATE,
	SALARY					NUMBER,
    ORG_ID					VARCHAR2(40), 
    PARENT_ORG_ID			VARCHAR2(40), 
    AREA_CODE				VARCHAR2(40),
	ORG_GROUP 				VARCHAR2(100),
	ORG_GROUP_ID 			VARCHAR2(60)
) 
TABLESPACE TBS_OSECDJW_RPT
NOLOGGING ;

CREATE INDEX OBJ_EMPLOYEE_IDX1 ON OBJ_EMPLOYEE (EMP_ID) TABLESPACE TBS_OSECDJW_RPT NOLOGGING;
CREATE INDEX OBJ_EMPLOYEE_IDX2 ON OBJ_EMPLOYEE (ORG_ID) TABLESPACE TBS_OSECDJW_RPT NOLOGGING;

COMMENT ON TABLE OBJ_EMPLOYEE  IS '人员数据查询';

