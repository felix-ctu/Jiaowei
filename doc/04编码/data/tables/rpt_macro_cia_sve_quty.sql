
DROP TABLE RPT_MACRO_CIA_SVE_QUTY CASCADE CONSTRAINTS PURGE;
CREATE TABLE RPT_MACRO_CIA_SVE_QUTY 
(	BUSINESS_DATE					DATE,
    CIA_DATE 						DATE, 
	FIRST_LAST_PUNCT_RATE 			NUMBER(12,2), 
	TRAIN_PUNCT_RATE 				NUMBER(12,2), 
	ONTIME_TRAIN_COUNT				NUMBER,
	TOTAL_TRAIN_COUNT				NUMBER,
	SHIFT_FULFILLED_RATE 			NUMBER(12,2), 
	DEPA_INTE_FULFILLED_RATE 		NUMBER(12,2),
	AREA_CODE 						VARCHAR2(30),
	ORG_GROUP 						VARCHAR2(100),
	ORG_GROUP_ID 					VARCHAR2(60)
) 
TABLESPACE TBS_OSECDJW_RPT
NOLOGGING ;


