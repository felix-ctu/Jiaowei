COMMENT ON COLUMN RPT_MACRO_CIA_SVE_QUTY.ONTIME_TRAIN_COUNT	 		IS '车次准点数量';
COMMENT ON COLUMN RPT_MACRO_CIA_SVE_QUTY.TOTAL_TRAIN_COUNT	 		IS '车次总数量';
COMMENT ON COLUMN RPT_MACRO_CIA_SVE_QUTY.SHIFT_FULFILLED_RATE 		IS '班次兑现率';
COMMENT ON COLUMN RPT_MACRO_CIA_SVE_QUTY.DEPA_INTE_FULFILLED_RATE 	IS '发车间隔兑现率';
COMMENT ON COLUMN RPT_MACRO_CIA_SVE_QUTY.AREA_CODE 					IS '区域类型';
COMMENT ON COLUMN RPT_MACRO_CIA_SVE_QUTY.ORG_GROUP 					IS '区域名称';
COMMENT ON COLUMN RPT_MACRO_CIA_SVE_QUTY.ORG_GROUP_ID 				IS '区域ID';

